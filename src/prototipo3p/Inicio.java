    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        
        if ( txtUsuario.getText().isEmpty() || txtPass.getText().isEmpty()){

            JOptionPane.showMessageDialog(null, " NO SE PUEDE DEJAR CAMPOS VACIOS");

            txtUsuario.setText("");
            txtPass.setText("");
        }
        else {
            try{

                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/siu", "root", "compromiso");
                PreparedStatement pst = cn.prepareStatement("select * from usuarios where nombre = ?");

                pst.setString(1, txtUsuario.getText().trim());
                ResultSet rs = pst.executeQuery();

                PreparedStatement pst2 = cn.prepareStatement("select * from usuarios where contrasenia = ?");

                pst2.setString(1, txtPass.getText().trim());
                ResultSet rs2 = pst2.executeQuery();

                if(rs.next()){

                    if(rs2.next()){
                        JOptionPane.showMessageDialog(null, "Bienvenido" + "  " + rs.getString("nombre"));

                        
                        txtUsuario.setText("");
                        txtPass.setText("");
                        Inicio ventana = new Inicio();
                        ventana.dispose();
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "ESTE USUARIO NO EXISTE");
                    txtUsuario.setText("");
                    txtPass.setText("");

                }

                //this.dispose();
            }catch(Exception e){

            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

