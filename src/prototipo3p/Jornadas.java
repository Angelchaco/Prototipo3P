    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       if(txtBuscar.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "NO SE REGISTRO NINGUNA JONADA DE BUSQUEDA");
        }else{
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/siu", "root", "compromiso");
            PreparedStatement pst = cn.prepareStatement("select * from jornadas where codigo_jornada = ?");
            pst.setString(1, txtBuscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txt_codigojornada.setText(rs.getString("codigo_jornada"));
                txt_nombrejornada.setText(rs.getString("nombre_jornada"));
                txt_estadojornada.setText(rs.getString("estatus_jornada"));
            } else {
                JOptionPane.showMessageDialog(this, "JORNADA NO REGISTRADA", "WARNING",JOptionPane.WARNING_MESSAGE);
            }

        }catch (Exception e){
                JOptionPane.showMessageDialog(this, "ERROR AL MOSTRAR REGISTRO", "ERROR",JOptionPane.ERROR_MESSAGE);

        }     
        
        } 
       
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        if(txt_codigojornada.getText().trim().isEmpty()||txt_nombrejornada.getText().trim().isEmpty() || txt_estadojornada.getText().trim().isEmpty()||txtBuscar.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this,"NO PUEDE HABER CAMPOS VACIOS");
        }else{
        try {
            String ID = txtBuscar.getText().trim();

            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/siu", "root", "compromiso");
            PreparedStatement pst = cn.prepareStatement("update jornadas set codigo_jornada= ?, nombre_jornada=?, estatus_jornada=?  where codigo_jornada= " + ID);

            pst.setString(1, txt_codigojornada.getText().trim());
            pst.setString(2, txt_nombrejornada.getText().trim());
            pst.setString(3, txt_estadojornada.getText().trim());
            pst.executeUpdate();

            txt_codigojornada.setText("");
            txt_nombrejornada.setText("");
            txt_estadojornada.setText("");
            JOptionPane.showMessageDialog(this, "MODIFICACION EXITOSO", "MENSAJE",JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR AL MODIFICAR ", "ERROR",JOptionPane.ERROR_MESSAGE);
        }}

    }//GEN-LAST:event_btnModificarActionPerformed

